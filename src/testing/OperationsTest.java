		byte[][] dd = (new Utilities(D)).getData();
		byte[][] dc = (new Utilities(D)).getData();
		byte[]   cb = Encoder.supahGauss(ab,db);
		boolean gaussMatchesD = true;
		boolean pidecMatchesD = true;
		
