    private static byte[][] initD(int Kprime) {

        // force single source block
        final FECParameters fecParams = FECParameters.newParameters(F, ceilDiv(F, Kprime), 1);
        final Random rand = TestingCommon.newSeededRandom();

        final byte[] data = TestingCommon.randomBytes(F, rand);
        final ArraySourceBlockEncoder enc = (ArraySourceBlockEncoder)OpenRQ.newEncoder(data, fecParams).sourceBlock(0);
        return ArraySourceBlockEncoder.forceInitVectorD(enc);
    }

    @Test
    public void testA_10() throws SingularMatrixException {

        final int Kprime = 10;
        final byte[][] D = initD(Kprime);
        final ByteMatrix A = LinearSystem.generateConstraintMatrix(Kprime);
        LinearSystem.PInactivationDecoding(A, D, Kprime);
        Assert.assertEquals(A_10, A);
    }


